package ZadaniaPetle;

public class Zadanie1 {
    public static void main(String[] args) {
        for (int i = 0; i <= 30; i++) {
            System.out.println(i + " ");
        }
        System.out.println();
        for (int i = 30; i >= 0; i--) {
            System.out.println(i + " ");
        }
    }
}
